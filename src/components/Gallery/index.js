class Gallery extends Component {
  state = {
    images: imagesList,
  }

  imageChange = id => {
    const filteredImage = imagesList.filter(each => each.id === id)
    this.setState({images: filteredImage})
  }

  render() {
    const {images} = this.state

    const urlOfImage = images[0].imageUrl
    const altImage = images[0].imageAltText

    return (
      <div className="main-container-white">
        <img src={urlOfImage} alt={altImage} />

        <h1 className="heading">Nature Photography</h1>

        <p className="paragraph">Nature Photography by Rahul</p>

        <ul className="un-order-list">
          {imagesList.map(each => (
            <ThumbnailItem
              imagesList={each}
              key={each.id}
              imageChange={this.imageChange}
            />
          ))}
        </ul>
      </div>
    )
  }
}
